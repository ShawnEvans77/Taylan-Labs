public class L2Q6 {
    public static void main(String[] args) {
        /**
         * 6) Compute the perimeter of a rectangle whose width is 10.4 in and height is
         * 12.2 in. And print the perimeter.
         */

        double width = 10.4;
        double height = 12.2;

        double perimeter = 2 * (width+height);

        System.out.println(perimeter);
    }

}